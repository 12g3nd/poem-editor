export function randomPrompt(excluding?: string): string {
  if (WRITING_PROMPTS.length <= 1) return WRITING_PROMPTS[0]
  let next = WRITING_PROMPTS[Math.floor(Math.random() * WRITING_PROMPTS.length)]
  while (next === excluding) {
    next = WRITING_PROMPTS[Math.floor(Math.random() * WRITING_PROMPTS.length)]
  }
  return next
}
